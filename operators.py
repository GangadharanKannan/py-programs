a = 5
b = 4

#arithmetic Operator
print(a+ b, a*b, a-b, a/b, a%b, a//b, a**b)

#Relational Operator
print(a>b, a<b, a==b, a>=b, a<=b, a!=b)

#Logical Operator
print(a>b and a!=b, a<b or a!=b, not(a<b))

#Augmented Assignment Operator
a += b
print(a)

#membership Operator
lst = [1,2,3,4,5,6]

print(2 in lst)
print (7 not in lst)
